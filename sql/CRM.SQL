
DROP DATABASE IF EXISTS CRM;

CREATE DATABASE CRM;
USE CRM;


CREATE TABLE Person (
    person_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    email VARCHAR(100),
    phone VARCHAR(20),
    address VARCHAR(255)
);

CREATE TABLE Employee (
    employee_id INT PRIMARY KEY AUTO_INCREMENT,
    person_id INT,
    job_title VARCHAR(50),
    department VARCHAR(50),
    salary DECIMAL(10,2),
    hire_date DATE,
    FOREIGN KEY (person_id) REFERENCES Person(person_id)
);

CREATE TABLE Customer (
    customer_id INT PRIMARY KEY AUTO_INCREMENT,
    person_id INT,
    company_name VARCHAR(100),
    registration_date DATE,
    total_spent DECIMAL(12,2),
    FOREIGN KEY (person_id) REFERENCES Person(person_id)
);

CREATE TABLE Leads (
  lead_id INT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(100) NOT NULL,
  email VARCHAR(100) NOT NULL,
  phone VARCHAR(20),
  source VARCHAR(100),
  status ENUM('New', 'Contacted', 'Qualified', 'Lost') DEFAULT 'New',
  employee_id INT,
  FOREIGN KEY (employee_id) REFERENCES Employee(employee_id)
);


CREATE TABLE Tickets (
  ticket_id INT PRIMARY KEY AUTO_INCREMENT,
  customer_id INT,
  issue_description TEXT,
  status ENUM('Open', 'In Progress', 'Closed') DEFAULT 'Open',
  created_date DATE,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id) ON DELETE CASCADE
);



-- Table: Opportunities (sales opportunities)
CREATE TABLE Opportunities (

  opportunity_id INT PRIMARY KEY AUTO_INCREMENT,
  customer_id INT,
  description VARCHAR(200),
  estimated_value DECIMAL(10,2),
  stage ENUM('Prospecting', 'Qualification', 'Proposal', 'Negotiation', 'Closed Won', 'Closed Lost') DEFAULT 'Prospecting',
  created_date DATE,
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id) ON DELETE CASCADE
);

-- Table: Activities (customer interactions)
CREATE TABLE Activities (
  activity_id INT PRIMARY KEY AUTO_INCREMENT,
  related_type ENUM('Opportunity', 'Campaign', 'Ticket', 'Lead'),
  related_id INT NOT NULL,
  activity_type ENUM('Call', 'Email', 'Meeting', 'Task'),
  activity_date DATE,
  notes TEXT,
   CONSTRAINT fk_related_entity FOREIGN KEY (related_id) REFERENCES Opportunities(opportunity_id) ON DELETE CASCADE
);
ALTER TABLE Activities DROP FOREIGN KEY fk_related_entity;



-- Table: Campaigns (marketing campaigns)
CREATE TABLE Campaigns (
  campaign_id INT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(100) NOT NULL,
  start_date DATE,
  end_date DATE,
  budget DECIMAL(10,2),
  employee_id INT,
  FOREIGN KEY (employee_id) REFERENCES Employee(employee_id)
);

CREATE TABLE Accounts (
  account_id INT PRIMARY KEY AUTO_INCREMENT,
  customer_id INT,
  account_number VARCHAR(50) NOT NULL UNIQUE,
  creation_date DATE,
  account_type ENUM('Standard', 'Premium'),
  balance DECIMAL(10,2),
  FOREIGN KEY (customer_id) REFERENCES Customer(customer_id) ON DELETE CASCADE
);


